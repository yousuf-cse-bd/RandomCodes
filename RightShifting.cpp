/****
    Right Shifting
    5 >> 1
    Here 1 means 5/2 and Flour
*/

#include <iostream>
using namespace std;

int main(int argc, char const* argv[])
{
    int num;
    num = 5;
    num = num >> 1; ///Equality to 5/2 and Flour
    cout<<"Number : "<<num<<endl;
    return 0;
}
